package Java_Programs;

public class LogicalOperator {

	
	public static void main(String[] args) {
		int x = 10;
		System.out.println(x>15 && x <20);
//	
//		true and true = true
//		true and false = false
//		false and false = false
//		false and true = false
//	
		System.out.println(x>15 || x <20);

//		true or true = true
//		true or false = true
//		false or false = false
//		false or true = true
		
		//! - not opertor
		
		System.out.println(!(x>15) && x <20);

		
		//bitwise
		//binary input
	//	5 - 0101
	//	1 - 0001
		
	}
}
